% Exemplul 1, metoda Gauss fara pivotare.
A = [
4 2 1 1;
0 1 1 3; 
2 1 5 5; 
]

A(3, :) += -1/2 * A(1, :)

A(1, :) += -2/1 * A(2, :);
A(3, :) += -0/1 * A(2, :)

A(1, :) += 1/4.5 * A(3,:);
A(2, :) += -1/4.5 * A(3,:)

A(3, :) /= A(3, 3);
A(1, :) /= A(1, 1)


% Exemplul 2, cu pivotare partiala.
B = [
0, 1, -2, 4;
1, -1, 1, 6;
1, 0, -1, 2;
]

s = B(1, :);
B(1,:) = B(2, :);
B(2, :) = s


B(2, :) += - B(2, 1) / B(1, 1) * B(1, :);
B(3, :) += - B(3, 1) / B(1, 1) * B(1, :)

B(1, :) += - B(1, 2) / B(2, 2) * B(2, :);
B(3, :) += - B(3, 2) / B(2, 2) * B(2, :)


s = B(1, :);
B(1,:) = B(2, :);
B(2, :) = s
% Pe ultima linie avem ca 0 = -8, deci nu exista solutie.

% Exemplul 3, cu pivotare completa.
C = [
4 2 1 1;
0 1 1 3; 
2 1 5 5; 
]

s = C(:, 1);
C(:, 1) = C(:, 3);
C(:, 3) = s;

s = C(1, :);
C(1, :) = C(3, :);
C(3, :) = s


C(2, :) += - C(2, 1) / C(1, 1) * C(1, :);
C(3, :) += - C(3, 1) / C(1, 1) * C(1, :)


s = C(:, 2);
C(:, 2) = C(:, 3);
C(:, 3) = s;

s = C(2, :);
C(2, :) = C(3, :);
C(3, :) = s

C(1, :) += - C(1, 2) / C(2, 2) * C(2, :);
C(3, :) += - C(3, 2) / C(2, 2) * C(2, :)


C(1, :) += - C(1, 3) / C(3, 3) * C(3, :);
C(2, :) += - C(2, 3) / C(3, 3) * C(3, :)


C(1, :) /= C(1, 1);
C(2, :) /= C(2, 2);
C(3, :) /= C(3, 3)